package br.com.brincandocomthreads.vo;

public class BoloBaseVO {
	
	private String ovo;
	
	private String fermento;
	
	private String farinha;

	public String getOvo() {
		return ovo;
	}

	public void setOvo(String ovo) {
		this.ovo = ovo;
	}

	public String getFermento() {
		return fermento;
	}

	public void setFermento(String fermento) {
		this.fermento = fermento;
	}

	public String getFarinha() {
		return farinha;
	}

	public void setFarinha(String farinha) {
		this.farinha = farinha;
	}
	
	
	
	

}
